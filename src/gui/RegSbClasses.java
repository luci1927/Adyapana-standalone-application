    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
                
        String subjectName = String.valueOf(jComboBox1.getSelectedItem());
        String className = String.valueOf(jComboBox2.getSelectedItem());



         if (subjectName.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Select a Marital Status", "Warning", JOptionPane.ERROR_MESSAGE);

        }  else if (className.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Select a Marital Status", "Warning", JOptionPane.ERROR_MESSAGE);

        } else {

            String subjectId = subjectMap.get(subjectName);
            String classId = classMap.get(className);
            
            try {

                MySQL.execute("INSERT INTO "
                        + "`class_has_subjects`(`subjects_subject_id`,`class_class_id`)"
                        + " VALUES ('" + subjectId + "','" + classId + "')");

                JOptionPane.showMessageDialog(this, "Successfully Added", "Success", JOptionPane.INFORMATION_MESSAGE);

                reset();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

