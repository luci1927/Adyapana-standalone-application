        String passaword = String.valueOf(jPasswordField1.getPassword());

        if (userId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid User ID", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (passaword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Password", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                ResultSet resultset = MySQL.execute("SELECT * FROM `academic_user` WHERE `user_id`='" + userId + "' AND `password`='" + passaword + "'");
                if (resultset.next()) {

                    String fname = resultset.getString("first_name");
                    String lname = resultset.getString("last_name");
                    String nic = resultset.getString("nic");
                    String mobileNo = resultset.getString("mobile_no");

                    Home home = new Home();
                    home.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Invalid details", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
