    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String paymentId = jTextField1.getText();

        String subjectName = String.valueOf(jComboBox1.getSelectedItem());
        String studentName = String.valueOf(jComboBox2.getSelectedItem());

        if (paymentId.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Enter Payment ID", "Warning", JOptionPane.ERROR_MESSAGE);


        } else if (studentName.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Select a Student", "Warning", JOptionPane.ERROR_MESSAGE);

        } else if (subjectName.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Select a Subject", "Warning", JOptionPane.ERROR_MESSAGE);

        } else {

            String subjectId = subjectMap.get(subjectName);
            String studentId = studentMap.get(studentName);

            try {

                MySQL.execute("INSERT INTO "
                        + "`payment`(`payment_id`,`subjects_subject_id`,`student_student_id`)"
                        + " VALUES ('" + paymentId + "','" + subjectId + "','" + studentId + "')");

                JOptionPane.showMessageDialog(this, "Successfully Added", "Success", JOptionPane.INFORMATION_MESSAGE);

                reset();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }


    }//GEN-LAST:event_jButton3ActionPerformed

