    private void loadSubjects() {

        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `subjects`");

            Vector v = new Vector();
            v.add("Select");

            while (resultSet.next()) {
                v.add(resultSet.getString("name"));
                subjectMap.put(resultSet.getString("name"), resultSet.getString("subject_id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(v);
            jComboBox1.setModel((model));

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadStudents() {

        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `student`");

            Vector v = new Vector();
            v.add("Select");

            while (resultSet.next()) {
                v.add(resultSet.getString("first_name"));
                studentMap.put(resultSet.getString("first_name"), resultSet.getString("student_id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(v);
            jComboBox2.setModel((model));

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

